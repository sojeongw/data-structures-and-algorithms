package CodeUp.Basic100.ConditionalOperation;

import java.util.Scanner;

public class N1077 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int n = 0;

        while(n<=a){
            System.out.println(n);
            n++;
        }
    }
}
